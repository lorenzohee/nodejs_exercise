/*
 Méthod calculGravityCenter
 */
function calculGravityCenter(x1, x2, y1, y2) {
    var x = x1 + (2 * (x2 - x1)) / 3;
    var y = y1 + (2 * (y2 - y1)) / 3;
    return {
        'x': x,
        'y': y
    };
};

