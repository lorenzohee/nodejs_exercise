/*
 Method hex2rgb
 */
function hex2rgb(hex, opacity) {
    var rgb = hex.replace('#', '').match(/(.{2})/g);

    var i = 3;
    while (i--) {
        rgb[i] = parseInt(rgb[i], 16);
    }

    if (typeof opacity == 'undefined') {
        return 'rgb(' + rgb.join(', ') + ')';
    }
    return 'rgba(' + rgb.join(', ') + ', ' + opacity + ')';
};
